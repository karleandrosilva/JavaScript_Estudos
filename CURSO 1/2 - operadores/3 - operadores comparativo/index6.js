// comparacação

// retornar verdadeiro ou falso

// maior que: >
console.log(10 > 5) // true
console.log(10 > 12) // false

// menor que: <
console.log(10 < 5) // false
console.log(10 < 12) // true

// maior ou igual a: >=
console.log(10 >= 5) // true
console.log(10 >= 10) // true

// menor ou igual a: <=
console.log(10 <= 5) // false
console.log(10 <= 10) // true

// comparação ou igual a: == 
console.log(10 == 5) // false
console.log(10 == 10) // true

// diferença ou diferente de: !=
console.log(10 != 5) // true
console.log(10 != 10) // false

// estritamente igual a: === (compara o tipo e o valor)
console.log(10 === 10) // true
console.log(10 === "10") // false, pois o tipo é diferente (number e string)
console.log("10" === "10") // true, pois o tipo é igual (string e string)

// estritamente diferente de: !== (compara o tipo e o valor)
console.log(10 !== 10) // false, pois o tipo e o valor são iguais (number e number)
console.log(10 !== "10") // true, pois o tipo é diferente (number e string)
console.log("10" !== "10") // false, pois o tipo e o valor são iguais (string e string)


