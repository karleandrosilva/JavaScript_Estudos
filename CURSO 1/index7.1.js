// operadores lógicos]

// AND - E (&&)
// OR - OU (||)
// NOT - NÃO (!)

const cake = true;
const pizza = true;

// (&&) só retorna verdadeiro se ambos forem verdadeiro
console.log(cake && pizza); // true, pois ambos são verdadeiros

// (||) retorna verdadeiro se pelo menos um for verdadeiro
console.log(cake || pizza); // true, pois pelo menos um é verdadeiro

const isOpen = true;
// (!) inverte o valor booleano 
console.log(!isOpen); // false, pois isOpen é true, então inverte para falso

