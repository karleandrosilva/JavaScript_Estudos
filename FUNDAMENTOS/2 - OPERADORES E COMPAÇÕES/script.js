// COMPARAÇÕES

// ==, ===, !=, !==

console.log(1 != 2); // diferente

console.log(1 !== 2); // se o valor é diferente e se o tipo é diferente
console.log(1 !== 1);

console.log("teste" == "teste"); 

// incremento 
let n = 1;

console.log(n);
console.log(n++); // só incrementa depois
console.log(n);

console.log(++n); // incrementa na hora
console.log(--n);


