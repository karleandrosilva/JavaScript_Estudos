console.log("OPERADORES");

let n1 = 10;
let n2 = 2
let n3 = "2";

console.log(n1 + n2); // adição
console.log(n1 - n2); // subtração
console.log(n1 * n2); // multiplicação
console.log(n1 / n2); // divisão
console.log(n1 % n2); // resto

console.log(n1 == n3); // igual a (conteudo)
console.log(n1 === n2); // identico a (conteudo e tipo)
console.log(n1 != n2); // diferente de não
console.log(n1 !== n2); // não é identico

console.log("OPERADORES RELACIONAIS");

console.log(n1 > n2); // maior
console.log(n1 >= n2); // maior ou igual

console.log(n1 < n2); // menor
console.log(n1 <= n2); // menor ou igual


